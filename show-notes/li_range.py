#Overview of Ranges in Python Lists

tags = ['python', 'development', 'tutorials', 'code']

tag_range = tags[2:]
  #['tutorials', 'code']

tag_range = tags[0:2]
  #['python', 'development']

tag_range = tags[:2]
  #['python', 'development']

tag_range = tags[0:-1]
  #['python', 'development', 'tutorials']

